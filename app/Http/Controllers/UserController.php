    public function roleUpdate(Request $request )
    {
        $user=User::find((int)$request->user_id);
        if($request->role_id == 0 ){
            $user->role_id = null;
            $user->save();
            return $user->name . ' has no role now';
        }
        $role=Roles::find((int)$request->role_id);
        
        $user->role_id=$role->id;
        $user->save();
        return $user->name . '\'s role has changed to ' . $user->role->name;
    }
